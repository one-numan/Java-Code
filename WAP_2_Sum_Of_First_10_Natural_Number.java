// Using Iteration Method
class Sum_Of_First_10_Natural_Number{


	// Creating Class
	public static void main(String args[]) {
	// Creating main Class
	
		int i;
		float sum=0;
		// Running Loop 
		for(i=1;i<=10;i++)
		{
		sum=sum+i;
		System.out.println(i);
		}
	System.out.println("Sum is :"+sum);
	}
}
